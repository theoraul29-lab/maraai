def main() -> None:
    out_dir = HERE / "locales"
    for lang, overlay in OVERRIDES.items():
        errors = AUTH_ERRORS.get(lang)
        if errors:
            overlay.setdefault("auth", {}).setdefault("errors", {}).update(errors)
        merged = merge(EN, overlay)
        (out_dir / f"{lang}.json").write_text(
            json.dumps(merged, indent=2, ensure_ascii=False) + "\n",
            encoding="utf-8",
        )
        print(f"wrote {lang}.json")


if __name__ == "__main__":
    main()
