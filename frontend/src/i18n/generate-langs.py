#!/usr/bin/env python3
"""
Generate locale JSON files for MaraAI i18n.

Design
------
- en.json is the canonical source. Every other language file is produced by
  applying a deep overlay on top of en.json, so any key not overridden falls
  back to English at load time (i18next also enforces this at runtime).
- Trading strategies (`trading.strategies.1..50`) deliberately stay in English.
  Trading strategy names / steps use universal industry vocabulary (EMA, RSI,
  MACD, SL/TP, Ichimoku, VWAP…) where translation would hurt rather than help.
- The 11 user-approved languages (en, ro, es, ru, uk, de, fr, ar, hi, pt, ja)
  get comprehensive overlays below. Other languages (it, pl, nl, cs, hu, bg,
  hr, sr, tr, ko, zh, th, vi, sv, da, el) keep only the core overlays they
  had in the previous generate-langs.cjs; anything missing falls back to EN.

Run
---
    cd frontend/src/i18n && python3 generate-langs.py
"""
from __future__ import annotations

import copy
import json
from pathlib import Path

HERE = Path(__file__).parent
EN = json.loads((HERE / "locales" / "en.json").read_text(encoding="utf-8"))


def merge(base: dict, overlay: dict) -> dict:
    """Deep-merge `overlay` into a copy of `base`."""
    out = copy.deepcopy(base)

    def _m(b, o):
        for k, v in o.items():
            if isinstance(v, dict) and isinstance(b.get(k), dict):
                _m(b[k], v)
            else:
                b[k] = v

    _m(out, overlay)
    return out


