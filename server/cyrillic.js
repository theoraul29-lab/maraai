export function detectCyrillicLang(text) {
  for (const char of text) {
    if (/[ґєії]/i.test(char)) return "UA";
    if (/[ёъы]/i.test(char)) return "RU";
  }
  for (const char of text) {
    if (/[а-яА-ЯёЁ]/i.test(char)) return "RU";
  }
  return "LATIN";
}
export function transliterate(text, lang = "RU") {
  const map = lang === "UA" ? cyrillicToLatinUA : cyrillicToLatinRU;
  let result = "";
  for (const char of text) {
    result += map[char] !== undefined ? map[char] : char;
  }
  return result;
}
export function hasCyrillic(text) {
  return /[а-яА-ЯёЁґєіїҐЄІЇ]/.test(text);
}
export function normalizeForSearch(text) {
  if (!hasCyrillic(text)) return text.toLowerCase();
  const lang = detectCyrillicLang(text);
  return transliterate(text, lang).toLowerCase();
}
